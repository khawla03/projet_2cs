package net.model;

public class Xy {
	private int idxy; 
	private float x; 
	private float y;
	public int getIdxy() {
		return idxy;
	}
	public void setIdxy(int idxy) {
		this.idxy = idxy;
	}
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	

}
